"""Python program to demonstrate the creation of Lists"""
# As of https://www.geeksforgeeks.org/python-lists/
# Basic List
List = []
print("Blank List: ")
print(List)

# List of numbers
List = [10, 33, 144]
print("\nList of numbers: ")
print(List)

# List of strings and access via 'index'
List = ["Geeks", "for", "Geeks"]
print("\List Items: ")
print(List[0])
print(List[2])
